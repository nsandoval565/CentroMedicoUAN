package Modelo;

public class Clientes {
	private String nombreCliente;
	private String apellidoCliente;
	private String identificacionCliente;
	private String edadCliente;
	private String telefonoCliente;
	private String citaMedico;
	private String citaServicio;
	private String fechaCita;
	private String horaInicioCita;
	private String horaFinCita;
	private String precioCita;
	
	public Clientes(){
		
	}

	public String getNombreCliente() {
		return nombreCliente;
	}

	public void setNombreCliente(String nombreCliente) {
		this.nombreCliente = nombreCliente;
	}

	public String getApellidoCliente() {
		return apellidoCliente;
	}

	public void setApellidoCliente(String apellidoCliente) {
		this.apellidoCliente = apellidoCliente;
	}

	public String getIdentificacionCliente() {
		return identificacionCliente;
	}

	public void setIdentificacionCliente(String identificacionCliente) {
		this.identificacionCliente = identificacionCliente;
	}

	public String getEdadCliente() {
		return edadCliente;
	}

	public void setEdadCliente(String edadCliente) {
		this.edadCliente = edadCliente;
	}

	public String getTelefonoCliente() {
		return telefonoCliente;
	}

	public void setTelefonoCliente(String telefonoCliente) {
		this.telefonoCliente = telefonoCliente;
	}

	public String getCitaMedico() {
		return citaMedico;
	}

	public void setCitaMedico(String citaMedico) {
		this.citaMedico = citaMedico;
	}

	public String getCitaServicio() {
		return citaServicio;
	}

	public void setCitaServicio(String citaServicio) {
		this.citaServicio = citaServicio;
	}

	public String getFechaCita() {
		return fechaCita;
	}

	public void setFechaCita(String fechaCita) {
		this.fechaCita = fechaCita;
	}

	public String getHoraInicioCita() {
		return horaInicioCita;
	}

	public void setHoraInicioCita(String horaInicioCita) {
		this.horaInicioCita = horaInicioCita;
	}

	public String getHoraFinCita() {
		return horaFinCita;
	}

	public void setHoraFinCita(String horaFinCita) {
		this.horaFinCita = horaFinCita;
	}

	public String getPrecioCita() {
		return precioCita;
	}

	public void setPrecioCita(String precioCita) {
		this.precioCita = precioCita;
	}
	
}
