package Modelo;

public class Servicios {
	private String nombreServicio;
	private String descripcionServicio;
	private String complejidadServicio;
	private String duracionServicio;
	private String costoServicio;

	public Servicios() {

	}

	public String getNombreServicio() {
		return nombreServicio;
	}

	public void setNombreServicio(String nombreServicio) {
		this.nombreServicio = nombreServicio;
	}

	public String getDescripcionServicio() {
		return descripcionServicio;
	}

	public void setDescripcionServicio(String descripcionServicio) {
		this.descripcionServicio = descripcionServicio;
	}

	public String getComplejidadServicio() {
		return complejidadServicio;
	}

	public void setComplejidadServicio(String complejidadServicio) {
		this.complejidadServicio = complejidadServicio;
	}

	public String getDuracionServicio() {
		return duracionServicio;
	}

	public void setDuracionServicio(String duracionServicio) {
		this.duracionServicio = duracionServicio;
	}

	public String getCostoServicio() {
		return costoServicio;
	}

	public void setCostoServicio(String costoServicio) {
		this.costoServicio = costoServicio;
	}

}
